export function getDemoButtonCopy(lang?: string | null): DemoButtonCopy {
  const l = (lang ?? "en").toLowerCase();

  // direct hits + common variants
  if (l.startsWith("it")) return I18N_DEMO_BUTTON.it;
  if (l.startsWith("fr")) return I18N_DEMO_BUTTON.fr;
  if (l.startsWith("es")) return I18N_DEMO_BUTTON.es;
  if (l.startsWith("de")) return I18N_DEMO_BUTTON.de;

  // Portuguese (pt / pt-br / pt-pt)
  if (l === "pt" || l.startsWith("pt-")) return I18N_DEMO_BUTTON.pt;

  if (l.startsWith("nl")) return I18N_DEMO_BUTTON.nl;
  if (l.startsWith("sv")) return I18N_DEMO_BUTTON.sv;

  // Norwegian (no / nb / nn)
  if (l === "no" || l.startsWith("nb") || l.startsWith("nn")) return I18N_DEMO_BUTTON.no;

  if (l.startsWith("da")) return I18N_DEMO_BUTTON.da;
  if (l.startsWith("fi")) return I18N_DEMO_BUTTON.fi;
  if (l.startsWith("pl")) return I18N_DEMO_BUTTON.pl;
  if (l.startsWith("tr")) return I18N_DEMO_BUTTON.tr;
  if (l.startsWith("el") || l.startsWith("gr")) return I18N_DEMO_BUTTON.el;
  if (l.startsWith("cs")) return I18N_DEMO_BUTTON.cs;
  if (l.startsWith("hu")) return I18N_DEMO_BUTTON.hu;
  if (l.startsWith("ro")) return I18N_DEMO_BUTTON.ro;
  if (l.startsWith("uk")) return I18N_DEMO_BUTTON.uk;
  if (l.startsWith("ru")) return I18N_DEMO_BUTTON.ru;

  // ✅ RTL (3)
  if (l.startsWith("ar")) return I18N_DEMO_BUTTON.ar;
  if (l.startsWith("he") || l.startsWith("iw")) return I18N_DEMO_BUTTON.he;
  if (l.startsWith("ur")) return I18N_DEMO_BUTTON.ur;

  if (l.startsWith("hi")) return I18N_DEMO_BUTTON.hi;
  if (l.startsWith("bn")) return I18N_DEMO_BUTTON.bn;
  if (l.startsWith("id")) return I18N_DEMO_BUTTON.id;
  if (l.startsWith("ms")) return I18N_DEMO_BUTTON.ms;
  if (l.startsWith("th")) return I18N_DEMO_BUTTON.th;
  if (l.startsWith("vi")) return I18N_DEMO_BUTTON.vi;
  if (l.startsWith("ja")) return I18N_DEMO_BUTTON.ja;
  if (l.startsWith("ko")) return I18N_DEMO_BUTTON.ko;
  if (l.startsWith("zh")) return I18N_DEMO_BUTTON.zh;

  return I18N_DEMO_BUTTON.en; // default to English
}

// Optional helper: if you ever want to check RTL at call-site
export function isDemoButtonRTL(lang?: string | null) {
  const l = (lang ?? "").toLowerCase();
  return l.startsWith("ar") || l.startsWith("he") || l.startsWith("iw") || l.startsWith("ur");
}
