export function getPrivacyCopy(lang: string | null | undefined): PrivacyCopy {
  const base = String(lang || "")
    .toLowerCase()
    .split("-")[0]; // handles zh-CN, pt-BR, fr-CA, etc.

  return PRIVACY_COPY[base as PrivacyLang] ?? PRIVACY_COPY.en;
}


export function isPrivacyLang(x: any): x is PrivacyLang {
  return typeof x === "string" && (PRIVACY_LANGS as readonly string[]).includes(x.toLowerCase());
}
